/** Equirectangular projection into the map's SVG coordinate space. */
export const MAP_VIEW = { width: 900, height: 990 } as const

export function project([lon, lat]: LonLat): [number, number] {
  const x = (lon + 122) * 10
  const y = (41 - lat) * 10
  return [x, y]
}

export function ringToPath(ring: LonLat[]): string {
  return (
    ring
      .map((point, index) => {
        const [x, y] = project(point)
        return `${index === 0 ? 'M' : 'L'}${x.toFixed(1)} ${y.toFixed(1)}`
      })
      .join(' ') + ' Z'
  )
}
